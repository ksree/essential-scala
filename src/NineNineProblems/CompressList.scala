package NineNineProblems

object CompressList extends App {
  def compress[A](input: List[A]): List[A] = input.foldLeft(List[A]())({ (r, e) =>
    if (r.isEmpty ||  r.head != e)
      e :: r
    else
      r
  }).reverse

  def compress2[A](ls: List[A]): List[A] =
    ls.foldRight(List[A]()) { (h, r) =>
      if (r.isEmpty || r.head != h) h :: r
      else r
    }

  assert(compress(List('a, 'a, 'a, 'a, 'b, 'c, 'c, 'a, 'a, 'd, 'e, 'e, 'e, 'e)) == List('a, 'b, 'c, 'a, 'd, 'e))
  assert(compress2(List('a, 'a, 'a, 'a, 'b, 'c, 'c, 'a, 'a, 'd, 'e, 'e, 'e, 'e)) == List('a, 'b, 'c, 'a, 'd, 'e))

}
